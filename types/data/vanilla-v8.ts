export type DataType = {
  items: Record<ItemEnum, ItemData>;
  buildings: Record<BuildingEnum, BuildingData>;
  floors: Record<FloorEnum, GameObjectBase>;
  beacons: Record<BeaconEnum, BeaconData>;
};

export interface GameObjectBase {
  inGameModes: GameModeEnum[];
  name: string;
  imageRow: number;
  imageCol: number;
}

export interface BuildingData extends GameObjectBase {
  power?: number;
  booster?: Partial<Record<ItemEnum, { speedBoost: number; perSec: number }>>;
  affinities?: Partial<
    Record<FloorEnum | WallsEnum, { affinity?: number; efficiency?: number }>
  >;
}

export interface ItemData extends GameObjectBase {
  producedBy: BuildingEnum[];
  group: ItemGroupEnum;
}

export interface BeaconData extends GameObjectBase {
  speedIncrease: number;
}

