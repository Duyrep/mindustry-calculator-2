export type DataType = {
  resources: Record<ResourceEnum, ResourceType>;
  extractors: Record<ExtractorEnum, ExtractorType>;
  factories: Record<FactoryEnum, FactoryType>;
  unitBuildings: Record<UnitBuildingEnum, UnitBuildingType>;
  beacons: Record<BeaconEnum, BeaconType>;
  recipes: Record<string, RecipeType>;
};

export type ResourceType = {
  inGameModes: GameModeEnum[];
  group: ResourceGroupEnum;
  producedBy: BuildingEnum[];
  key: string;
  localizedName?: Record<string, string>;
  imageRow: number;
  imageCol: number;
};

export type UnitBuildingType = {
  inGameModes: GameModeEnum[];
  energyUsage: number;
  key: string;
  localizedName?: Record<string, string>;
  imageRow: number;
  imageCol: number;
};

export type FactoryType = {
  inGameModes: GameModeEnum[];
  energyUsage: number;
  key: string;
  localizedName?: Record<string, string>;
  imageRow: number;
  imageCol: number;
  affinities?: Partial<
    Record<
      FloorsEnum,
      {
        affinity?: number;
        efficiency?: number;
      }
    >
  >;
};

export type ExtractorType = {
  inGameModes: GameModeEnum[];
  energyUsage: number;
  key: string;
  localizedName?: Record<string, string>;
  imageRow: number;
  imageCol: number;
  booster?: Partial<
    Record<
      ResourceEnum,
      {
        perSec: number;
        speed: number;
      }
    >
  >;
  affinities?: Partial<
    Record<
      FloorsEnum,
      {
        affinity?: number;
        efficiency?: number;
      }
    >
  >;
  resourceRates?: Partial<Record<ResourceEnum, number>>;
};

export type BeaconType = {
  inGameModes: GameModeEnum[];
  distributionEffectivity: number;
  energyUsage: number;
  localizedName?: Record<string, string>;
  imageRow: number;
  imageCol: number;
};

export type RecipeType = {
  buildings: Partial<
    Record<
      BuildingEnum,
      {
        input: {
          name: ResourceEnum | UnitEnum;
          perSec: number;
        }[];
        output: {
          name: ResourceEnum | UnitEnum;
          perSec: number;
        }[];
      }
    >
  >;
  localizedName?: Record<string, string>;
  imageRow: number;
  imageCol: number;
};

