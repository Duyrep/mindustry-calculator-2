export type DataType = {
  resources: Record<ResourceEnum, ResourceType>;
  units: Record<UnitEnum, UnitType>;
  extractors: Record<ExtractorEnum, ExtractorType>;
  factories: Record<FactoryEnum, FactoryType>;
  unitBuildings: Record<UnitBuildingEnum, UnitBuildingType>;
  beacons: Record<BeaconEnum, BeaconType>;
  terrain: Record<TerrainEnum, TerrainType>;
  recipes: Record<string, RecipeType>;
};

export type ResourceType = {
  inGameModes: GameModeEnum[];
  group: GameObjectGroupEnum;
  producedBy: BuildingEnum[];
  key: string;
  imageRow: number;
  imageCol: number;
};

export type TerrainType = {
  inGameModes: GameModeEnum[];
  key: string;
  imageRow: number;
  imageCol: number;
};

export type UnitType = {
  inGameModes: GameModeEnum[];
  group: GameObjectGroupEnum;
  producedBy: BuildingEnum[];
  key: string;
  imageRow: number;
  imageCol: number;
};

export type UnitBuildingType = {
  inGameModes: GameModeEnum[];
  energyUsage: number;
  key: string;
  imageRow: number;
  imageCol: number;
};

export type FactoryType = {
  inGameModes: GameModeEnum[];
  energyUsage: number;
  key: string;
  imageRow: number;
  imageCol: number;
  affinities?: Partial<
    Record<
      TerrainEnum,
      {
        affinity?: number;
        efficiency?: number;
      }
    >
  >;
};

export type ExtractorType = {
  inGameModes: GameModeEnum[];
  energyUsage: number;
  key: string;
  imageRow: number;
  imageCol: number;
  booster?: Partial<
    Record<
      ResourceEnum,
      {
        perSec: number;
        speed: number;
      }
    >
  >;
  affinities?: Partial<
    Record<
      TerrainEnum,
      {
        affinity?: number;
        efficiency?: number;
      }
    >
  >;
  resourceRates?: Partial<Record<ResourceEnum, number>>;
};

export type BeaconType = {
  inGameModes: GameModeEnum[];
  distributionEffectivity: number;
  energyUsage: number;
  imageRow: number;
  imageCol: number;
};

export type RecipeType = Partial<
  Record<
    BuildingEnum | UnitBuildingEnum,
    {
      timeToProduce: number;
      ingredients?: {
        name: ResourceEnum | UnitEnum;
        amount: number;
      }[];
      results: {
        name: ResourceEnum | UnitEnum;
        amount: number;
      }[];
      imageRow?: number;
      imageCol?: number;
    }
  >
>;

